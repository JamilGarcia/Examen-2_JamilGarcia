    public void CargarTec() throws IOException {
        File f = null;
        FileReader fr = null;
        BufferedReader br = null;
        try {
            f = new File("./tecnicos.txt");
            fr = new FileReader(f);
            br = new BufferedReader(fr);
            String linea;
            String[] tokens;
            while ((linea = br.readLine()) != null) {
                tokens = linea.split("/");
                tecni.add(new Tecnicos(tokens[0], Integer.parseInt(tokens[1]), tokens[2]));
                tokens = null;
            }

        } catch (Exception e) {
            e.printStackTrace();

        }
        br.close();
        fr.close();
    }

    public void CargarTodo() throws IOException {
        if (cargado == false) {
            System.out.println("Cargando");
            CargarTec();
            AdminComputadora ac = new AdminComputadora("./ficheros/computadora.tcr");
            AdminPartes ap = new AdminPartes("./ficheros/partes.tcr");
            ac.cargarArchivo();
            ap.cargarArchivo();
            comp = ac.getListaC();
            parte = ap.getListaP();
            ActualizarCombox();
            cargado = true;
            System.out.println("Cargó");
        }
    }

    public void GuardarTodo() throws IOException {
        GuardarTec();
        AdminComputadora ac = new AdminComputadora("./ficheros/computadora.tcr");
        AdminPartes ap = new AdminPartes("./ficheros/partes.tcr");
        ac.cargarArchivo();
        for (int i = 0; i < comp.size(); i++) {
            ac.setComp(comp.get(i));
        }
        ac.escribirArchivo();
        ap.cargarArchivo();
        for (int i = 0; i < parte.size(); i++) {
            ap.setPartes(parte.get(i));
        }
        ap.escribirArchivo();
        ActualizarCombox();
    }

    public void ActualizarCombox() {
        try {
            DefaultComboBoxModel lcomp = (DefaultComboBoxModel) cb_Computadoras.getModel();
            DefaultComboBoxModel ltecni = (DefaultComboBoxModel) cb_tecnicos.getModel();
            DefaultComboBoxModel ltecni2 = (DefaultComboBoxModel) cb_tecnico.getModel();
            DefaultComboBoxModel lbate = (DefaultComboBoxModel) cb_Bateria.getModel();
            DefaultComboBoxModel ldisco = (DefaultComboBoxModel) cb_DIscoD.getModel();
            DefaultComboBoxModel lram = (DefaultComboBoxModel) cb_RAM.getModel();
            DefaultComboBoxModel lpan = (DefaultComboBoxModel) cb_pantalla.getModel();
            DefaultComboBoxModel lpro = (DefaultComboBoxModel) cb_procesador.getModel();
            DefaultComboBoxModel ltecl = (DefaultComboBoxModel) cb_teclado.getModel();

            lcomp.removeAllElements();
            lbate.removeAllElements();
            ldisco.removeAllElements();
            lpan.removeAllElements();
            lpro.removeAllElements();
            lram.removeAllElements();
            ltecl.removeAllElements();
            ltecni.removeAllElements();
            ltecni2.removeAllElements();

            for (int i = 0; i < comp.size(); i++) {
                lcomp.addElement(comp.get(i));
            }

            for (int i = 0; i < tecni.size(); i++) {
                ltecni.addElement(tecni.get(i));
                ltecni2.addElement(tecni.get(i));
            }

            for (int i = 0; i < parte.size(); i++) {
                if (parte.get(i) instanceof Bateria) {
                    lbate.addElement(parte.get(i));
                } else if (parte.get(i) instanceof DiscoDuro) {
                    ldisco.addElement(parte.get(i));
                } else if (parte.get(i) instanceof RAM) {
                    lram.addElement(parte.get(i));
                } else if (parte.get(i) instanceof Pantalla) {
                    lpan.addElement(parte.get(i));
                } else if (parte.get(i) instanceof Procesador) {
                    lpro.addElement(parte.get(i));
                } else if (parte.get(i) instanceof Teclado) {
                    ltecl.addElement(parte.get(i));
                };
            }
            cb_Bateria.setModel(lbate);
            cb_Computadoras.setModel(lcomp);
            cb_DIscoD.setModel(ldisco);
            cb_RAM.setModel(lram);
            cb_pantalla.setModel(lpan);
            cb_procesador.setModel(lpro);
            cb_teclado.setModel(ltecl);
            cb_tecnico.setModel(ltecni);
            cb_tecnicos.setModel(ltecni2);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

