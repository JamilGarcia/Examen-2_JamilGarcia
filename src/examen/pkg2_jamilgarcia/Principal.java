    public void fallar(Computadora comp, Tecnicos tecni) {
        int flag = tecni.getComputadorasT(), num = ran.nextInt(100);

        if (flag < 6) {
            if (num < 30) {
                informe(comp.toString(), tecni.toString());
            }
        } else if (flag > 5 && flag < 16) {
            if (num < 22) {
                informe(comp.toString(), tecni.toString());
            }
        } else if (flag > 15 && flag < 31) {
            if (num < 13) {
                informe(comp.toString(), tecni.toString());
            }
        } else {
            if (num < 7) {
                informe(comp.toString(), tecni.toString());
            }
        }
    }

